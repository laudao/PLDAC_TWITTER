import re

POSITIVE = ["\*O", "\*-\*", "\*O\*", "\*o\*", "\* \*",
                ":P", ":D", ":d", ":p",
                ";P", ";D", ";d", ";p",
                ":-\)", ";-\)", ":=\)", ";=\)",
                ":<\)", ":>\)", ";>\)", ";=\)",
                "=\}", ":\)", "\(:;\)",
                "\(;", ":\}", "\{:","<3", ";\}",
                "\{;:\]",
                "\[;", ":'\)", ";'\)", ":-3",
                "\{;", ":\]",
                ";-3", ":-x", ";-x", ":-X",
                ";-X", ":-\}", ";-=\}", ":-\]",
                ";-\]", ":-.\)",
                "\^_\^", "\^-\^","\^\^"]

NEGATIVE = [":\(", ";\(", ":'\(",
                "=\(", "=\{", "\):", "\);",
                "\)':", "\)';", "\)=", "\}=",
                ";-\{\{", ";-\{", ":-\{\{", ":-\{",
                ":-\(", ";-\(",
                ":,\)", ":'\{",
                "\[:", ";\]"
                ]


def ParseTweet(tweet):
    print("|".join(POSITIVE))
    formatted_s = re.sub(r"|".join(POSITIVE),"positif", tweet)
    formatted_s = re.sub(r"|".join(NEGATIVE), "negatif", formatted_s)
    return formatted_s
